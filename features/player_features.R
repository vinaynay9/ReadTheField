# Player features capture both recent performance and longer-term historical patterns to inform model predictions.
# The output consists of standardized feature vectors that can be combined with other feature sets in the modeling pipeline.
# This module serves as a critical component in the feature engineering stage, providing the player-specific inputs required for model training.
