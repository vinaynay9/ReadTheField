# Defense Features

# This file contains functions for extracting and engineering defensive unit features from processed data sources.
# It computes features that characterize defensive performance including points allowed, yards allowed, and efficiency metrics.
# The module generates features at both team and positional unit levels to capture different aspects of defensive capability.
# Defense features incorporate opponent-adjusted statistics to account for strength of schedule and contextual factors.
# These features enable models to account for defensive matchups when making predictions about player performance.
# The output provides standardized defensive feature vectors that complement player and game context features in the modeling workflow.
